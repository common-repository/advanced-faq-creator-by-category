					</td>
				</tr>

			</table>
			<?php

		}
		/*
* حفظ الخيارات الي جايه من السكشن الي فوق
*/
		public function save_metabox( $post_id, $post ) {

			
			if ( ! current_user_can( 'edit_post', $post_id ) )
				return;

			if ( wp_is_post_autosave( $post_id ) )
				return;

			if ( wp_is_post_revision( $post_id ) )
				return;

			$q_icon_color = isset( $_POST[ 'q_icon_color' ] ) ? sanitize_text_field( $_POST[ 'q_icon_color' ] ) : '';
			$icon_font_select = isset( $_POST[ 'icon_font_select' ] ) ? sanitize_text_field( $_POST[ 'icon_font_select' ] ) : '';

			update_post_meta( $post_id, 'q_icon_color', $q_icon_color );
			update_post_meta( $post_id, 'icon_font_select', $icon_font_select );

		}
