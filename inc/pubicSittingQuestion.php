										<option selected value="<?php echo esc_attr($key);?>" data-icon="<?php echo esc_attr($key);?>"><?php echo esc_attr($val);?></option>';
									<?php }else{ ?>
										<option value="<?php echo esc_attr($key);?>" data-icon="<?php echo esc_attr($key);?>"><?php echo esc_attr($val);?></option>';
