			$q_icon_color = esc_attr(get_post_meta( $post->ID, 'q_icon_color', true ));
			$icon_font_select = esc_attr(get_post_meta( $post->ID, 'icon_font_select', true ));

			if( empty( $q_icon_color ) ) $q_icon_color = '#000000';
			if( empty( $icon_font_select ) ) $icon_font_select = 'no-icon';

			?>
			<table class="form-table">

				<tr>
                    <td><label for="q_icon_color" class="gwp_pc_primary_color_label"><?php echo esc_html__( 'Icon color', 'advanced-faq-creator-by-category' );?></label></td>
                    <td>
                        <input type="text" id="q_icon_color" name="q_icon_color" class="gwp_pc_primary_color_field"  value="<?php echo  esc_attr__( $q_icon_color );?>">
                        <p class="description"><?php echo esc_html__( 'Select icon color', 'advanced-faq-creator-by-category' );?></p>
                    </td>
					
					<th><label for="icon_font_select"><?php echo esc_html__( 'Select icon', 'advanced-faq-creator-by-category' );?></label></th>
					<td>
						
						<select id="icon_font_select" class="select2-icon" name="icon_font_select" style="font-family:'FontAwesome';    display: none;">
